def main():
    parser = argparse.ArgumentParser(description="Automated report emailer")
    parser.add_argument("--to",       nargs="+", default=["recipient@example.com"],
                        help="Recipient email addresses")
    parser.add_argument("--subject",  default=f"Daily KPI Report {datetime.now().strftime('%Y-%m-%d')}")
    parser.add_argument("--db",       default=REPORT_DB,    help="Source SQLite database")
    parser.add_argument("--attach",   nargs="*", default=[], help="Extra file paths to attach")
    parser.add_argument("--dry-run",  action="store_true",  help="Build email but do not send")
    parser.add_argument("--smtp-user",default=SMTP_USER)
    parser.add_argument("--smtp-pass",default=SMTP_PASS)
    args = parser.parse_args()

    # Seed demo data
    seed_demo_db(args.db)

    # Build report
    df      = load_kpis(args.db)
    html    = build_html_report(df)
    sender  = args.smtp_user or "noreply@example.com"

    msg = build_email(
        sender=sender,
        recipients=args.to,
        subject=args.subject,
        html_body=html,
        attachment_paths=args.attach,
    )

    send_email(
        msg,
        smtp_host=SMTP_HOST,
        smtp_port=SMTP_PORT,
        smtp_user=args.smtp_user,
        smtp_pass=args.smtp_pass,
        dry_run=args.dry_run,
    )


if __name__ == "__main__":
    main()
