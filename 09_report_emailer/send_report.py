# REPORT BUILDER
# ---------------------------------------------------------------------------

def load_kpis(db_path: str, table: str = "daily_kpis") -> pd.DataFrame:
    conn = sqlite3.connect(db_path)
    df = pd.read_sql_query(f"SELECT * FROM {table} ORDER BY report_date DESC", conn)
    conn.close()
    return df


def build_html_report(df: pd.DataFrame, title: str = "Daily KPI Report") -> str:
    """Generate a styled HTML email body from a DataFrame."""
    run_ts  = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    total_rev = df["revenue"].sum() if "revenue" in df.columns else "N/A"
    total_ord = df["orders"].sum()  if "orders"  in df.columns else "N/A"

    table_html = df.to_html(
        index=False,
        border=0,
        classes="data-table",
        float_format="{:,.2f}".format,
    )

    html = f"""
    <!DOCTYPE html>
    <html>
    <head>
      <style>
        body        {{ font-family: Arial, sans-serif; color: #333; }}
        h1          {{ color: #1F4E79; }}
        .summary    {{ background: #D6E4F0; padding: 12px; border-radius: 6px; margin-bottom: 16px; }}
        .data-table {{ border-collapse: collapse; width: 100%; }}
        .data-table th {{ background: #1F4E79; color: #fff; padding: 8px 12px; text-align: left; }}
        .data-table td {{ padding: 7px 12px; border-bottom: 1px solid #ddd; }}
        .data-table tr:nth-child(even) {{ background: #f2f8fd; }}
        .footer     {{ font-size: 11px; color: #999; margin-top: 24px; }}
      </style>
    </head>
    <body>
      <h1>{title}</h1>
      <div class="summary">
        <strong>Generated:</strong> {run_ts}<br>
        <strong>Total Revenue:</strong> ${total_rev:,.2f}<br>
        <strong>Total Orders:</strong> {total_ord:,}
      </div>
      {table_html}
      <p class="footer">Auto-generated by the Automated Report Emailer &mdash; Project 9</p>
    </body>
    </html>
    """
    return html


# ---------------------------------------------------------------------------
# EMAIL BUILDER + SENDER
# ---------------------------------------------------------------------------

def build_email(
    sender: str,
    recipients: list[str],
    subject: str,
    html_body: str,
    attachment_paths: list[str] = None,
) -> MIMEMultipart:
    msg = MIMEMultipart("mixed")
    msg["From"]    = sender
    msg["To"]      = ", ".join(recipients)
    msg["Subject"] = subject

    # HTML body
    alt = MIMEMultipart("alternative")
    alt.attach(MIMEText(html_body, "html"))
    msg.attach(alt)

    # Attachments
    for path in (attachment_paths or []):
        p = Path(path)
        if not p.exists():
            log.warning("Attachment not found, skipping: %s", path)
            continue
        with open(p, "rb") as f:
            part = MIMEBase("application", "octet-stream")
            part.set_payload(f.read())
        encoders.encode_base64(part)
        part.add_header(
            "Content-Disposition",
            f"attachment; filename={p.name}",
        )
        msg.attach(part)
        log.info("Attached: %s", p.name)

    return msg


def send_email(
    msg: MIMEMultipart,
    smtp_host: str,
    smtp_port: int,
    smtp_user: str,
    smtp_pass: str,
    dry_run: bool = False,
):
    if dry_run:
        log.info("[DRY RUN] Would send email to: %s", msg["To"])
        log.info("[DRY RUN] Subject: %s", msg["Subject"])
        return

    with smtplib.SMTP(smtp_host, smtp_port) as server:
        server.ehlo()
        server.starttls()
        server.login(smtp_user, smtp_pass)
        server.sendmail(
            msg["From"],
            msg["To"].split(", "),
            msg.as_string(),
        )
    log.info("Email sent successfully to: %s", msg["To"])


